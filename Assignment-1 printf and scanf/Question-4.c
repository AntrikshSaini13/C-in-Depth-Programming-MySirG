// Write a program to print \n on screen
#include<stdio.h>
int main()
{
    printf("1 hello students\n"); //for new line
    printf("2 hello\tstudents\n");//for tab space
    printf("3 hello \bstudents\n");//backspace
    printf("4 hello\rstudents\n");//before words are remove
    printf("5 hello\fstudents\n"); //new
    printf("6 hello\\students\n");
    printf("\"7 hello students\"\n");
    printf("\'8 hello students\'\n");
    printf("\\n");
}